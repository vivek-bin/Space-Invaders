// Interrupt service routine
void SysTick_Handler(){
	unsigned long pposx;
	unsigned char switches;
	int i;
	frames++;
	if((player.life<1)||(eshipsleft<1)||(gamestartflag==0))
		return;
	
	switches=GPIO_PORTE_RIS_R&0x03;
	GPIO_PORTE_ICR_R |= 0x03;
	
	if((gamestartflag==0)&&(switches!=0))
	{
		gamestartflag=1;
		return;
	}
	pposx=ADC0_In()/44;
	player.x=pposx-27;
	
	if((switches==0x01)||(switches==0x02)||(switches==0x03))
	{
		for(i=0;i<3;i++)
  		if((player.x+PLAYERW/2 > bunkers[i].x + 2)&&(player.x + PLAYERW/2 < bunkers[i].x + BUNKERW-2)&&(bunkers[i].life!=0))
			{fireflag=1;break;}
		    
		if((num_pbullets<MAXPBUL)&&(fireflag==0))
		{
			fireflag=1;
			for(i=0;i<MAXPBUL;i++)
				if(pbullets[i].life==0)
				{
			    num_pbullets++;
				  pbullets[i].life=1;
				  pbullets[i].x=player.x + PLAYERW/2;
				  pbullets[i].y=player.y - PLAYERH;
				  pbullets[i].pic=Laser0;
				  break;
				}
		}
	}
	displayflag=1;
}



