void EBul_interact(){
	int i,j;
	if(num_ebullets==0)
		return;
	for(i=0;i<MAXEBUL;i++)
	{
		if(ebullets[i].life==0)
			continue;
		if(ebullets[i].y<SCREENH-PLAYERH-BUNKERH)
			continue;
		if(ebullets[i].y<SCREENH-PLAYERH-2)
			for(j=0;j<3;j++)
		  {
				if(bunkers[j].life==0)
					continue;
				if((ebullets[i].x > bunkers[j].x + 1)&&(ebullets[i].x + LASERW < bunkers[j].x + BUNKERW-1))
				{
					GPIO_PORTB_DATA_R|=0x20;
					frames=0;
					bunkers[j].life--;
					if(bunkers[j].life==1)
						bunkers[j].pic=Bunker2;
					else if(bunkers[j].life==0)
						bunkers[j].pic=Bunker3;
					ebullets[i].life=0;
					num_ebullets--;
					break;
				}
			}	
		if(ebullets[i].life==0)
			continue;
		if(ebullets[i].y<SCREENH-PLAYERH-1)
			continue;
		if((ebullets[i].x > player.x + 1)&&(ebullets[i].x + LASERW < player.x + PLAYERW-1))
				{
					GPIO_PORTB_DATA_R|=0x20;
					frames=0;
					player.life--;
					ebullets[i].life=0;
					num_ebullets--;
				}
	}
}

